package com.tscp.mvne.network.exception;

public class UsageDetailAccessException extends Exception {
  private static final long serialVersionUID = -2644233997664532369L;

  public UsageDetailAccessException() {
    super();
  }

  public UsageDetailAccessException(String message, Throwable cause) {
    super(message, cause);
  }

  public UsageDetailAccessException(String message) {
    super(message);
  }

  public UsageDetailAccessException(Throwable cause) {
    super(cause);
  }

}
